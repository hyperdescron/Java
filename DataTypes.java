public class DataTypes {
    public static void main(String[] args) {
        // In java data types are separated in 2 groups
        // Primitive data type
        // Non-primitive(Reference) data type

        /*
         * Primitive data types are 
         * byte
         * short
         * int
         * long
         * float
         * double
         * char
         * boolean
         */

         // Non-primitive data type examples are String,Array,ArrayList etc...

         // As we can see primitive data types begins with lowercase letters
         // Where as non-primitive data types begins with uppercase letters
        
         byte byteNum = 5;
         short shortNum = 10;
         int intNum = 15;
         long longNum = -4L;
         float floatNum = 3.14F;
         double doubleNum = 5.17;
         char charVal = 'T';
         boolean booleanVal = true;

         String name = "Tunc";

         System.out.println(byteNum);
         System.out.println(shortNum);
         System.out.println(intNum);
         System.out.println(longNum);
         System.out.println(floatNum);
         System.out.println(doubleNum);
         System.out.println(charVal);
         System.out.println(booleanVal);
         System.out.println(name);
    }
}
